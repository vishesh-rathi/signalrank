"""Lexicon coverage audit (addresses critical_review R1).

The technical-fit axis keys off a hand-curated STRONG lexicon. The fair critique
is: what is its recall — how many genuine builders does it miss because they use
a synonym the lexicon does not list? This script measures that directly against
the real pool, so the answer is data, not assertion.

    uv run python eval/lexicon_coverage.py

Generates eval/lexicon_coverage_report.md. Read-only.

Finding (this synthetic pool is templated): the off-lexicon builder vocabulary the
review hypothesises ("discovery engine", "search relevance pipeline", "query
understanding", "two-tower", "solr", ...) is essentially absent from candidate
summaries; the only off-lexicon builder term that appears is "collaborative
filtering". So lexicon recall is narrow *by design* yet near-fully-covering here.
"""

import sys
from collections import Counter
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from ranker.features import evidence_concepts
from ranker.util import build_evidence_texts, compile_lexicon, iter_candidates

CANDIDATES_PATH = "candidates.jsonl"

# Builder vocabulary that denotes search/ranking/rec/retrieval system work but is
# deliberately NOT in config.STRONG_PHRASES — the recall test set.
OFF_LEXICON = [
    "discovery engine", "discovery system", "search relevance", "relevance engineering",
    "query understanding", "feed ranking", "feed ranker", "matching engine",
    "matching algorithm", "matchmaking", "recommendation pipeline", "ad ranking",
    "content ranking", "two-tower", "two tower", "collaborative filtering",
    "content-based filtering", "nearest neighbor", "approximate nearest neighbor",
    "ann index", "solr", "lucene", "vespa", "algolia", "typesense", "hnsw", "scann",
    "annoy", "personalization engine", "ranker", "ranking pipeline", "search pipeline",
    "search ranking", "ltr",
]


def main() -> None:
    off_any = compile_lexicon(tuple(OFF_LEXICON))
    off_each = {term: compile_lexicon((term,)) for term in OFF_LEXICON}

    n = 0
    concept_hist = Counter()           # distinct primary STRONG concepts {0,1,2,3+}
    in_summary = Counter()             # off-lexicon term -> count in summaries
    summary_hit = 0                    # candidates with any off-lexicon term in summary
    desc_only_hit = 0                  # off-lexicon only in (boilerplate) descriptions
    for c in iter_candidates(CANDIDATES_PATH):
        n += 1
        summ = ((c.get("profile") or {}).get("summary") or "").lower()
        descs = " ".join(
            (e.get("description") or "") for e in (c.get("career_history") or [])
        ).lower()
        ev_text, _ = build_evidence_texts(c)
        concept_hist[min(len(evidence_concepts(ev_text.lower())), 3)] += 1
        s_hit = off_any.search(summ) is not None
        if s_hit:
            summary_hit += 1
            for term, pat in off_each.items():
                if pat.search(summ):
                    in_summary[term] += 1
        elif off_any.search(descs):
            desc_only_hit += 1

    detected = concept_hist[1] + concept_hist[2] + concept_hist[3]
    found = [(t, k) for t, k in in_summary.most_common() if k]
    missing = [t for t in OFF_LEXICON if t not in in_summary]

    out = [
        "# Lexicon Coverage Report (generated by eval/lexicon_coverage.py)\n",
        f"Pool size: **{n}**.\n",
        "## Primary STRONG-concept depth distribution",
        "Distinct concepts in the primary evidence text (summary + role titles):\n",
        "| distinct concepts | candidates | evidence grade |",
        "|---|---|---|",
        f"| 0 | {concept_hist[0]} | 0.5 (generic ML) / 0.6 (corroborated) / 0.0 |",
        f"| 1 | {concept_hist[1]} | 0.70 |",
        f"| 2 | {concept_hist[2]} | 0.85 |",
        f"| 3+ | {concept_hist[3]} | 1.00 |",
        f"\nThe lexicon credits builder-depth for **{detected}** candidates "
        f"({detected / n:.3%} of the pool) — high precision, intentionally low recall.\n",
        "## Off-lexicon builder-vocabulary recall test",
        f"{len(OFF_LEXICON)} builder synonyms NOT in the lexicon were scanned.\n",
        f"- Candidates using any in their **summary** (author-written): **{summary_hit}**",
        f"- Candidates using any only in **descriptions** (recycled boilerplate, "
        f"non-distinctive): {desc_only_hit}\n",
        "### Off-lexicon terms that actually appear in summaries",
        "| term | summary occurrences |",
        "|---|---|",
    ]
    for term, k in found:
        out.append(f"| {term} | {k} |")
    if not found:
        out.append("| (none) | 0 |")
    out += [
        f"\n**{len(missing)} of {len(OFF_LEXICON)}** tested synonyms have ZERO summary "
        "occurrences, including every system-noun the review names "
        "(discovery engine, search relevance, query understanding, two-tower, solr, …).\n",
        "## Conclusion",
        "The pool is templated, so candidate summaries draw from a fixed vocabulary. "
        "The hand lexicon was built against that vocabulary, so its narrow recall is "
        "near-fully-covering here: the only genuine off-lexicon builder term is "
        "`collaborative filtering`. Whether that one term should be credited as a "
        "recommendation concept is a scoring judgment evaluated separately "
        "(see eval/impact.py); it is not evidence of broad lexicon under-coverage.",
    ]
    Path("eval/lexicon_coverage_report.md").write_text("\n".join(out) + "\n")
    print(f"wrote eval/lexicon_coverage_report.md (pool={n}, builder-depth credited={detected}, "
          f"off-lexicon-in-summary={summary_hit}, terms-found={len(found)})")


if __name__ == "__main__":
    main()
